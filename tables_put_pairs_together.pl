% Purpose: Use to factorise the Size and NCol so that we have less facts
%          (called manually to generate part of tables.pl for the tables of a given combinatorial object)
%          the table facts are writen in the console by gen_metadata.pl and need to be copied in this file
% Author : Nicolas Beldiceanu, IMT Atlantique

% Each table has the following five arguments:
%  . the name of the table
%  . the value of MaxN (i.e. the maximum number of vertices) used to generate the table
%  . the number of columns of the table
%  . the number of parameters of the lower/upper bound
%  . the lower/upper bound considered

:- dynamic table/5.

top :-
    findall(Name, table(Name,_,_,_,_), TableNames),
    sort(TableNames, Names),
    gen_table_params(Names).

gen_table_params([]) :- !.
gen_table_params([Name|R]) :-
    findall(Size-NCol, table(Name, Size, NCol, _, _), Pairs),
    once(table(Name, _, _, NParams, Bound)),
%   write('table(graph, '),
%   write('table(forest, '),
%   write('table(forest0, '),
%   write('table(tree, '),
%   write('table(partition, '),
    write('table(partition0, '),
%   write('table(group, '),
%   write('table(cgroup, '),
    write(Name),
    write(', '),
    write(Pairs),
    write(', '),
    write(NParams),
    write(', '),
    write(Bound),
    write(').'),
    nl,
    gen_table_params(R).

